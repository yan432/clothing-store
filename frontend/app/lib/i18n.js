export function normalizeLocale(locale) {
  return locale === UK_LOCALE ? UK_LOCALE : DEFAULT_LOCALE
}

export function getMessages(locale) {
  return messages[normalizeLocale(locale)]
}

export function localeFromPathname(pathname = '') {
  return pathname === PUBLIC_UK_PREFIX || pathname.startsWith(`${PUBLIC_UK_PREFIX}/`)
    ? UK_LOCALE
    : DEFAULT_LOCALE
}

export function stripLocale(path = '/') {
  if (!path) return '/'
  if (path === PUBLIC_UK_PREFIX) return '/'
  if (path.startsWith(`${PUBLIC_UK_PREFIX}/`)) {
    return path.slice(PUBLIC_UK_PREFIX.length) || '/'
  }
  return path
}

export function pathForLocale(path = '/', locale = DEFAULT_LOCALE) {
  if (!path) return path
  if (/^(https?:|mailto:|tel:|#)/.test(path)) return path
  const stripped = stripLocale(path)
  const normalized = stripped.startsWith('/') ? stripped : `/${stripped}`
  return normalizeLocale(locale) === UK_LOCALE
    ? normalized === '/' ? PUBLIC_UK_PREFIX : `${PUBLIC_UK_PREFIX}${normalized}`
    : normalized
}

function splitPathExtras(pathname = '/') {
  const match = String(pathname).match(/^([^?#]*)(.*)$/)
  return { base: match?.[1] || '/', extras: match?.[2] || '' }
}

export function switchLocalePath(pathname = '/', locale = DEFAULT_LOCALE) {
  const { base, extras } = splitPathExtras(pathname)
  const stripped = stripLocale(base)
  const supported =
    stripped === '/' ||
    stripped === '/products' ||
    stripped.startsWith('/products/') ||
    stripped.startsWith('/collections/') ||
    stripped === '/about' ||
    stripped === '/contact' ||
    stripped === '/shipping' ||
    stripped === '/returns' ||
    stripped === '/faq' ||
    stripped === '/size-guide' ||
    stripped === '/cart' ||
    stripped === '/checkout' ||
    stripped === '/confirm' ||
    stripped === '/success' ||
    stripped === '/wishlist' ||
    stripped === '/auth' ||
    stripped === '/auth/reset' ||
    stripped === '/account' ||
    stripped.startsWith('/account/') ||
    stripped === '/privacy' ||
    stripped === '/terms' ||
    stripped === '/imprint'

  if (!supported) return normalizeLocale(locale) === UK_LOCALE ? PUBLIC_UK_PREFIX : '/'
  return `${pathForLocale(stripped, locale)}${extras}`
}

export function translateCategory(category, locale = DEFAULT_LOCALE) {
  const label = String(category || '')
  return getMessages(locale).categories[label] || label
}

export function getProductField(product, field, locale = DEFAULT_LOCALE) {
  if (!product) return ''
  const safeLocale = normalizeLocale(locale)
  if (safeLocale !== DEFAULT_LOCALE) {
    const localized = product[`${field}_${safeLocale}`]
    if (localized != null && String(localized).trim()) return localized
  }
  return product[field] || ''
}

export function localizeProduct(product, locale = DEFAULT_LOCALE) {
  if (!product) return product
  return {
    ...product,
    name: getProductField(product, 'name', locale) || product.name || '',
    description: getProductField(product, 'description', locale),
    material_care: getProductField(product, 'material_care', locale),
    product_details: getProductField(product, 'product_details', locale),
    fit_info: getProductField(product, 'fit_info', locale),
  }
}
